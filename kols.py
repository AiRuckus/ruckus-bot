def get_kol_profile(username):
    username = username.lower().replace("@", "")
    # Case insensitive lookup
    for handle, profile in PRAISED_KOLS.items():
        if handle.lower() == username:
            return {"tier": "praised", "profile": profile, "handle": handle}
    for handle, profile in DEMONIZED_KOLS.items():
        if handle.lower() == username:
            return {"tier": "demonized", "profile": profile, "handle": handle}
    if username in [k.lower() for k in WATCHLIST_KOLS]:
        return {"tier": "watchlist", "profile": None}
    return None

def is_watched_kol(username):
    return get_kol_profile(username) is not None

